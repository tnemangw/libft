#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	unsigned int	cnt;

	if (!s || !f)
		return ;
	cnt = 0;
	while (s[cnt] != '\0')
	{
		f(s + cnt);
		cnt++;
	}
}
